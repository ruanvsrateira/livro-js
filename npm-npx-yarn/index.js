/*
  Npm (Node Package Manager) -> Utilizado para gerenciar bibliotecas em um projeto nodejs
  Serve para atualizar bibliotecas já instaladas, registradas no package.json.
  -> npm update
  Serve para instalar alguma biblioteca para utilizar no projeto.
  -> npm install <nome-lib>

  NPX (Node Package Executable) -> Utilizado para rodar comandos de bibliotecas sem necessariamente instalar a biblioteca globalmente utilizando o npm.
  É usado para verificar se há atualizações disponível para as bibliotecas que estão presente no projeto.
  -> npx npm-check 
*/

/*
  Yarn -> Alternativa de Package manager para o NPM.
  Instalação do Yarn:
  -> npm i -g yarn
  -> yarn set version 2
  Verificação da versão do Yarn
  -> yarn -v

  Assim como o NPM tem o NPX para executar comandos de bibliotecas sem precisar instalalas globalmente, o Yarn tem o dlx
*/
